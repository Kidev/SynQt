def emit_replica_header(syn: SynFile, lstem: str) -> str:
    return "\n".join([SPDX_CPP, "#pragma once", "",
                      f"void synqtRegister{_cap(syn.stem)}Replicas();", ""])


def emit_replica_source(syn: SynFile, lstem: str) -> str:
    out = [SPDX_CPP,
           f'#include "{lstem}_replica.h"', "",
           f'#include "{lstem}_rep.h"', "",
           "#include <QQmlEngine>", "",
           "// When this Replica is built into a client that links the SynQt client runtime,"
           " register a",
           "// typed-Replica factory: a compile-time Replica carries its API and so syncs"
           " reliably,",
           "// including in the browser (a dynamic Replica's API-definition exchange does"
           " not complete",
           "// under single-threaded WASM). A minimal replica-only target that does not link"
           " the",
           "// runtime simply skips this and still builds.",
           "#if __has_include(<replicaregistry.h>)",
           "#  include <QRemoteObjectNode>",
           "#  include <replicaregistry.h>",
           "#  define SYNQT_REPLICA_FACTORY 1",
           "#endif", "",
           f"void synqtRegister{_cap(syn.stem)}Replicas()",
           "{"]
    for contract in syn.contracts:
        out.append(
            f'    qmlRegisterUncreatableType<{contract.name}Replica>("SynQt", 1, 0,'
        )
        out.append(
            f'        "{contract.name}Replica",'
        )
        out.append(
            f'        QStringLiteral("{contract.name}Replica is acquired from the SynQt '
            'runtime, not constructed in QML"));'
        )
        out.append("#ifdef SYNQT_REPLICA_FACTORY")
        out.append(
            f'    SynQt::registerReplicaFactory(QStringLiteral("{contract.name}"),'
        )
        out.append(
            "        [](QRemoteObjectNode *node, const QString &connectPoint) -> QObject * {"
        )
        out.append(
            f"            return node->acquire<{contract.name}Replica>(connectPoint);"
        )
        out.append("        });")
        out.append("#endif")
    out.append("}")
    out.append("")
    return "\n".join(out)
